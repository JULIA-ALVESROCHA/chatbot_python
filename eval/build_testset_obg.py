def build(target, seed):
    # gold corpus: one row per intent
    with open("gold_corpus.jsonl","w",encoding="utf-8") as f:
        for it in INTENTS:
            f.write(json.dumps({"id":it["id"],"intent":it["intent"],
                                "gold_answer":it["gold_answer"]},ensure_ascii=False)+"\n")

    # round-robin across intents for even coverage, deterministic
    pools = [list(it["q"]) for it in INTENTS]
    units, idx = [], 0
    for col in itertools.zip_longest(*pools):
        for i, q in enumerate(col):
            if q is None: continue
            lang, persona, text = q
            it = INTENTS[i]
            units.append({"id":f"{it['id']}__{persona}__{idx:03d}",
                          "intent":it["intent"],"lang":lang,"persona":persona,
                          "question":text,"gold_answer":it["gold_answer"],
                          "generated_answer":None})
            idx += 1
    units = units[:target]
    with open("testset_questions.jsonl","w",encoding="utf-8") as f:
        for u in units:
            f.write(json.dumps(u,ensure_ascii=False)+"\n")

    from collections import Counter
    print(f"intents={len(INTENTS)}  questions_written={len(units)}")
    print("by persona:", dict(Counter(u["persona"] for u in units)))
    print("by lang:", dict(Counter(u["lang"] for u in units)))

if __name__=="__main__":
    ap=argparse.ArgumentParser()
    ap.add_argument("--target",type=int,default=200)
    ap.add_argument("--seed",type=int,default=13)
    a=ap.parse_args()
    build(a.target,a.seed)